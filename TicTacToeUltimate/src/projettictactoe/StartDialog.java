    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        this.specifications.put("type", (String)this.jComboBox1.getSelectedItem());
        this.specifications.put("player1", this.nom1.getText());
        this.specifications.put("player2", this.nom2.getText());
        Principale principale  = new Principale(this.specifications,this.depth,this.robotStart);
       
        this.dispose();
        principale.setLocationRelativeTo(null);
        principale.setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        System.out.println(this.jComboBox1.getSelectedItem());
        if(this.jComboBox1.getSelectedItem().equals("Robot")){
            this.nom2.setText("Robot");
            this.nom2.setEnabled(false);
            this.jDepth.setVisible(true);
            this.jComboBox3.setVisible(true);
        }
           if(!this.jComboBox1.getSelectedItem().equals("Robot")){
               
            this.nom2.setText("");
            this.nom2.setEnabled(true);
            this.jDepth.setVisible(false);
            this.jComboBox3.setVisible(false);
        }

    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void jDepthActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jDepthActionPerformed
       if(this.jDepth.getSelectedItem().equals("Easy")){
            this.depth = 3;
        }
       if(this.jDepth.getSelectedItem().equals("Medium")){
            this.depth = 6;
        }
       if(this.jDepth.getSelectedItem().equals("Hard")){
            this.depth = 9;
        }
        
        
    }//GEN-LAST:event_jDepthActionPerformed

